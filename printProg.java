public class printProg {
    public static void main(String args[]) {

        /* Print statements in Java Language */

        System.out.println("Hello"); // prints "Hello" with '/n'
        System.out.print("Hi"); // prints "Hi" without '/n'
        System.out.print('\n'); // prints '\n' (a newline character)
        int x = 5;
        System.out.println(x); // prints an integer value, x
        double y = 2.232;
        System.out.println(y); // prints a double value , y

        String z = "Hello Java";
        System.out.print(z); // prints a string , z

    }
}
