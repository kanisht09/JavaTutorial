public class DataTypes {
    public static void main(String[] args) {

        /* Data types in Java -> only primitive shown here */

        byte age = 5;
        System.out.println(age); // prints a value of type byte, age

        /* Here 5 is called a literal */

        short k = 5;
        System.out.println(k); // prints a value of type short, k

        char cq = 'A';
        System.out.println(cq); // prints a character value , cq

        int x = 5;
        System.out.println(x); // prints an integer value, x

        /* double is a default literal for decimal numbers */

        double y = 2.232;
        System.out.println(y); // prints a double value , y

        float yf = 2.232f; // we need to write f in the end of the decimal number
        System.out.println(yf); // prints a float value, yf

        long kl = 2839393L; // we need to write L or l in the end of a long number
        System.out.println(kl); // prints a long value, k

        boolean c = true;
        System.out.println(c); // prints a boolean value, c

        c = false;
        System.out.println(c);

    }
}
